# Demo of relations operators
# Declare two variables, assign some values
a = 10
b = 20
# Perform Relational Operations and display directly using cat function
cat(" a = ", a, "b = ", b, "\n",
    "a == b : ", a == b , "\n",
    "a != b : ", a != b , "\n",
    "a < b : ", a < b , "\n",
    "a > b : ", a > b , "\n",
    "a <= b : ", a <= b , "\n",
    "a >= b : ", a >= b , "\n"
   ) 
